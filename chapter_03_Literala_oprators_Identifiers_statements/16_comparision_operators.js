// == and ===
// == Loose comparison (==) - checks only for the value
// === Strict comparison (===) - checks for both value and type

// < , > , <= , >= , != , !==
// != Not equal to (loose)
// !== Not strictly equal to (strict)

console.log(4 == "4"); // true
console.log(4 === "4"); // false
console.log(4 != "4"); // false
console.log(4 !== "4"); // true this is strictly coupled comparission
// !=== this does not exist in JavaScript , JavaScript  only has == and ===

